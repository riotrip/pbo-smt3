    private void ipkTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ipkTextKeyReleased
        String ipk = ipkText.getText();

        if (ipk.length() > 4) {
            JOptionPane.showMessageDialog(null, "IPK tidak valid", "Peringatan", JOptionPane.WARNING_MESSAGE);
            ipkText.setText(""); 
            return;
        }

        try {
            double ipkValue = Double.parseDouble(ipk);

            if (ipkValue < 0.00 || ipkValue > 4.00) {
                JOptionPane.showMessageDialog(null, "IPK tidak valid", "Peringatan", JOptionPane.WARNING_MESSAGE);
                ipkText.setText(""); 
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "IPK tidak valid", "Peringatan", JOptionPane.WARNING_MESSAGE);
            ipkText.setText(""); 
        }
    }//GEN-LAST:event_ipkTextKeyReleased

    private void tgllahirTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tgllahirTextKeyReleased
        String tanggalLahir = tgllahirText.getText();

        if (tanggalLahir.length() == 10) {
            if (!tanggalLahir.matches("\\d{4}-\\d{2}-\\d{2}")) {
                JOptionPane.showMessageDialog(null, "Format tanggal tidak valid. Harap gunakan format YYYY-MM-DD", "Kesalahan", JOptionPane.ERROR_MESSAGE);
                tgllahirText.setText(""); 
                return; 
            }

            try {
                LocalDate birthDate = LocalDate.parse(tanggalLahir);
                LocalDate currentDate = LocalDate.now();

                int age = Period.between(birthDate, currentDate).getYears();

                if (age < 18) {
                    JOptionPane.showMessageDialog(null, "Anda belum cukup umur untuk melamar pekerjaan ini", "Peringatan", JOptionPane.WARNING_MESSAGE);
                    tgllahirText.setText(""); 
                }
            } catch (DateTimeParseException e) {
                JOptionPane.showMessageDialog(null, "Format tanggal tidak valid. Harap gunakan format YYYY-MM-DD", "Kesalahan", JOptionPane.ERROR_MESSAGE);
                tgllahirText.setText("");
            }
        }
    }//GEN-LAST:event_tgllahirTextKeyReleased

    private void telpTextKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_telpTextKeyTyped
       char typedChar = evt.getKeyChar();

       if (!Character.isDigit(typedChar) && typedChar != '\b') {
           evt.consume(); 
       }
    }//GEN-LAST:event_telpTextKeyTyped

