    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        StringBuilder sb = new StringBuilder();

        if (nikText.getText().isEmpty() || namaText.getText().isEmpty() || alamatField.getText().isEmpty() ||
            genderGroup.getSelection() == null || telpText.getText().isEmpty() || tgllahirText.getText().isEmpty() ||
            pendidikanComboBox.getSelectedItem() == null || ipkText.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return; 
        } 
        
       String pendidikan = pendidikanComboBox.getSelectedItem().toString();
       String[] pendidikanLevels = {"SD", "SMP", "SMA", "D1", "D2", "D3", "D4 / S1", "S2", "S3"};

       int pendidikanIndex = Arrays.asList(pendidikanLevels).indexOf(pendidikan);

       if (pendidikanIndex < 3) {
           JOptionPane.showMessageDialog(this, "Pendidikan minimal D1!", "Peringatan", JOptionPane.WARNING_MESSAGE);
           return; 
       }

        try {
            double ipk = Double.parseDouble(ipkText.getText());
            if (ipk < 3.01) {
                JOptionPane.showMessageDialog(this, "IPK minimal 3,01!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return; 
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "IPK harus berupa angka valid!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return; 
        }     
        
        sb.append("NIK: ").append(nikText.getText()).append("\n");
        sb.append("Nama: ").append(namaText.getText()).append("\n");
        sb.append("Alamat: ").append(alamatField.getText()).append("\n");

        ButtonModel genderModel = genderGroup.getSelection();
        sb.append("Gender: ").append(genderModel != null ? genderModel.getActionCommand() : "-").append("\n");

        if (!makanCheckBox.isSelected() && !tidurCheckBox.isSelected() && !bermainCheckBox.isSelected() && !membacaCheckBox.isSelected()) {
            JOptionPane.showMessageDialog(this, "Pilih minimal satu hobi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return; 
        }        
        
        sb.append("Hobi: ");
        if (makanCheckBox.isSelected()) sb.append(makanCheckBox.getText()).append("\n");
        if (tidurCheckBox.isSelected()) sb.append(tidurCheckBox.getText()).append("\n");
        if (bermainCheckBox.isSelected()) sb.append(bermainCheckBox.getText()).append("\n");
        if (membacaCheckBox.isSelected()) sb.append(membacaCheckBox.getText()).append("\n");
       

        sb.append("No. Telp: ").append(telpText.getText()).append("\n");
        sb.append("Tanggal Lahir: ").append(tgllahirText.getText()).append("\n");
        
        Object pendidikanObject = pendidikanComboBox.getSelectedItem();
        sb.append("Pendidikan: ").append(pendidikanObject != null ? pendidikanObject : "-").append("\n");
        
        sb.append("IPK: ").append(ipkText.getText()).append("\n");
        
        JOptionPane.showMessageDialog(this, sb, "Biodata", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_okButtonActionPerformed

